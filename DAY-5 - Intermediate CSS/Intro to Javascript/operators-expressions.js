//Arithmetic operations
let x = 30 //Assigning a value
let y = 15 //Assigning a value

//get sum or total '+'
let sum = x + y;
console.log("Sum:", sum);

//get difference '-'
let difference = x - y;
console.log("Difference:", difference);

//get product '*'
let product = x * y;
console.log("product:", product)

//get quotient '/'
let quotient = x / y;
console.log("quotient:", quotient)

//Assignment expressions
let a = 10;
let b = 5;

// Addition '+='
a += b
console.log("a += b is", a);

//Subtraction '-='
a -= b
console.log("a -= b is", a);

//Multiplication '*='
a *= b
console.log("a *= b is", a);

//Division '/='
a /= b
console.log("a /= b is", a)

//get modulus '%'
let remainder = x % y;
console.log("Remainder:", remainder)

//concatenation
const firstWord = "Hello"
const secondWord = "World!"

console.log(firstWord + " " + secondWord)

//logical expression
let sunny = true;
let warm = false;

console.log("Is it sunny AND warm?", sunny && warm);
console.log("Is it sunnyOR warm?", sunny || warm);
console.log("Is it NOT sunny?", !sunny);
